    public void Signomenos() {
    String signos = this.calculos.getText();
    
        if (signos.startsWith("-")) 
            this.calculos.setText(signos.substring(1));
         
        else 
            this.calculos.setText("-" + signos);
    }
    
    public String cero_no(double resul)
    {
        
        String retorno = "";
        retorno = Double.toString(resul);
        
        if(resul%1 == 0)
        {
            retorno = retorno.substring(0, retorno.length()-2);
            
        }
        
        return retorno;
    }
