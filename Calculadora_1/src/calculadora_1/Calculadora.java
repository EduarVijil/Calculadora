    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        this.n2 = Double.parseDouble(this.calculos.getText());
        
        
        double porciento = n2*0.01;
        switch(this.operador)
        {
            case "+": this.calculos.setText(cero_no(this.n1+this.n2));
            break;
            
            case "-":this.calculos.setText(cero_no(this.n1-this.n2));
            break;
            
            case "x": this.calculos.setText(cero_no(this.n1*this.n2));
            break;
            
            case "%": this.calculos.setText(cero_no(this.n1*porciento));
            break;
            
            case "/": 
                if(this.n1 == 0 || this.n2 == 0)
                    this.calculos.setText("Syntax Error");
                else
                    this.calculos.setText(cero_no(this.n1/this.n2));
            break;
            
        }
        
        double resultado = 0;
        String n3 = cero_no(n1);
        String n4 = cero_no(n2);
        
        switch(this.operador) {
            case "+": 
                resultado = this.n1 + this.n2;
                break;
            case "-":
                resultado = this.n1 - this.n2;
                break;
            case "x": 
                resultado = this.n1 * this.n2;
                break;
            case "%": 
                resultado = this.n1 * (this.n2 * 0.01);
                break;
            case "/": 
                if(this.n1 == 0 || this.n2 == 0) {
                    System.out.println(n3 + " " + this.operador + " "+ n4+ " = "+ "Syntax Error");
                    return;
                } else {
                    resultado = this.n1 / this.n2;
                }
                break;
        }
        
        String resultadoStr = cero_no(resultado);
        
        String ecuacion = n3 + " " + this.operador + " " + n4 + " = ";
        System.out.println(ecuacion + resultadoStr);
    }//GEN-LAST:event_igualActionPerformed

